#ifndef CORE_TYPES_H
#define CORE_TYPES_H

#include <stdbool.h>
#include <stddef.h>
#include <stdint.h>
#include <uchar.h>
#include <wchar.h>

typedef int8_t i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;

typedef uint8_t u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;

typedef ptrdiff_t isize;
typedef size_t usize;

typedef float f32;
typedef double f64;

typedef unsigned char byte;
typedef unsigned char uchar;
typedef i32 b32;

typedef char c8;
typedef char16_t c16;
typedef char32_t c32;
typedef wchar_t wchar;

#endif // CORE_TYPES_H
